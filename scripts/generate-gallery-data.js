/**
 * Scans public/babershop/ and public/barbers/<name>/ folders
 * and generates src/data/generatedGallery.ts with all image paths.
 *
 * Run: node scripts/generate-gallery-data.js
 * Wired into dev & build via package.json scripts.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const PUBLIC = path.join(ROOT, "public");

const IMAGE_EXTENSIONS = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif", ".gif"]);

function isImage(file) {
  return IMAGE_EXTENSIONS.has(path.extname(file).toLowerCase());
}

/** Check if a filename is a before/after image */
function isBeforeAfter(file) {
  const name = path.basename(file).toLowerCase();
  return name.startsWith("before-") || name.startsWith("after-");
}

function readImages(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir).filter(isImage).sort();
}

// ── Barbershop gallery ──
const babershopDir = path.join(PUBLIC, "babershop");
const babershopFiles = readImages(babershopDir);

// ── Barber galleries ──
const barbersDir = path.join(PUBLIC, "barbers");
const barberFolders = fs.existsSync(barbersDir)
  ? fs
      .readdirSync(barbersDir)
      .filter((f) => fs.statSync(path.join(barbersDir, f)).isDirectory())
  : [];

const barberGalleries = {};
for (const folder of barberFolders) {
  barberGalleries[folder] = readImages(path.join(barbersDir, folder));
}

// ── Generate TypeScript file ──
let output = `/**\n * AUTO-GENERATED by scripts/generate-gallery-data.js\n * Do not edit manually — re-run the script after adding/removing photos.\n */\nimport type { GalleryImage } from "../components/LightboxGallery";\n\n`;

// Barbershop images
output += `export const barbershopImages: GalleryImage[] = [\n`;
for (const file of babershopFiles) {
  const src = `/babershop/${file}`;
  output += `  { src: ${JSON.stringify(src)}, alt: "Kingdom Place barbershop" },\n`;
}
output += `];\n\n`;

// Barber images — one exported array per folder (excluding before/after files)
for (const [folder, files] of Object.entries(barberGalleries)) {
  const varName = `${folder.toLowerCase()}Gallery`;
  const regularFiles = files.filter((f) => !isBeforeAfter(f));
  output += `export const ${varName}: GalleryImage[] = [\n`;
  for (const file of regularFiles) {
    const src = `/barbers/${folder}/${file}`;
    const alt = `${folder} — Barber work`;
    output += `  { src: ${JSON.stringify(src)}, alt: ${JSON.stringify(alt)} },\n`;
  }
  output += `];\n\n`;

  // Detect before/after pairs — always generate the export (empty if none)
  const beforeFiles = files.filter((f) => f.toLowerCase().startsWith("before-"));
  const pairs = [];
  for (const bf of beforeFiles) {
    const suffix = bf.replace(/^before-/i, "");
    const af = files.find(
      (f) => f.toLowerCase().startsWith("after-") && f.replace(/^after-/i, "") === suffix,
    );
    if (af) {
      pairs.push({ before: bf, after: af });
    }
  }
  const pairsVar = `${folder.toLowerCase()}BeforeAfter`;
  output += `export const ${pairsVar}: { before: GalleryImage; after: GalleryImage }[] = [\n`;
  for (const p of pairs) {
    const bSrc = `/barbers/${folder}/${p.before}`;
    const aSrc = `/barbers/${folder}/${p.after}`;
    output += `  {\n`;
    output += `    before: { src: ${JSON.stringify(bSrc)}, alt: "${folder} — Before" },\n`;
    output += `    after: { src: ${JSON.stringify(aSrc)}, alt: "${folder} — After" },\n`;
    output += `  },\n`;
  }
  output += `];\n\n`;
}

// Convenience map: barber folder name (lowercase) → gallery array
output += `export const barberGalleryMap: Record<string, GalleryImage[]> = {\n`;
for (const folder of Object.keys(barberGalleries)) {
  output += `  ${folder.toLowerCase()}: ${folder.toLowerCase()}Gallery,\n`;
}
output += `};\n\n`;

// Before/after map: barber folder name (lowercase) → pairs array
output += `export const beforeAfterMap: Record<string, { before: GalleryImage; after: GalleryImage }[]> = {\n`;
for (const folder of Object.keys(barberGalleries)) {
  output += `  ${folder.toLowerCase()}: ${folder.toLowerCase()}BeforeAfter,\n`;
}
output += `};\n`;

const outPath = path.join(ROOT, "src", "data", "generatedGallery.ts");
fs.writeFileSync(outPath, output, "utf-8");

console.log(`✔ Generated ${outPath}`);
console.log(`  barbershop: ${babershopFiles.length} images`);
for (const [folder, files] of Object.entries(barberGalleries)) {
  console.log(`  ${folder}: ${files.length} images`);
}
